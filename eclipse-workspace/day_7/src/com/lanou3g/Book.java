package com.lanou3g;

public class Book {
	private int bookSN;
	private String name;
	private double price;
	private String bookMan;
	public Book() {
		super();
	}
	public int getBookSN() {
		return bookSN;
	}
	public void setBookSN(int bookSN) {
		this.bookSN = bookSN;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	public String getBookMan() {
		return bookMan;
	}
	public void setBookMan(String bookMan) {
		this.bookMan = bookMan;
	}
	
	
}
